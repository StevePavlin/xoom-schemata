<template>
    <v-card class="xs12" height="95vh" width="100%" id="schemata-view-context">
        <v-card-title>Context</v-card-title>
        <v-card-text>
            <v-form
                    ref="form"
                    v-model="valid"
            >
                <v-row>
                    <v-col class="d-flex" cols="12">
                        <v-text-field
                                v-model="contextId"
                                label="ContextID"
                                disabled
                        ></v-text-field>
                    </v-col>
                    <v-col class="d-flex" cols="12" sm="6">
                        <v-autocomplete
                                :items="organizations"
                                label="Organization"
                                :rules="[rules.notEmpty]"
                                :loading="loading.organizations"
                                return-object
                                item-value="organizationId"
                                item-text="name"
                                v-model="organizationId"

                        ></v-autocomplete>
                    </v-col>
                    <v-col class="d-flex" cols="12" sm="6">
                        <v-autocomplete
                                :items="units"
                                label="Unit"
                                :rules="[rules.notEmpty]"
                                :loading="loading.units"
                                return-object
                                item-value="unitId"
                                item-text="name"
                                v-model="unitId"

                        ></v-autocomplete>
                    </v-col>
                    <v-col class="d-flex" cols="12">
                        <v-text-field
                                v-model="namespace"
                                :rules="[rules.notEmpty]"
                                label="Namespace"
                                required
                        ></v-text-field>
                    </v-col>
                </v-row>
                <v-row>
                    <v-col class="d-flex" cols="12">
                        <v-textarea
                                v-model="description"
                                :rules="[rules.notEmpty]"
                                label="Description"
                                required
                        ></v-textarea>
                    </v-col>
                </v-row>
            </v-form>
        </v-card-text>
        <v-card-actions>
            <v-btn color="info" @click="clearForm">New</v-btn>
            <v-spacer></v-spacer>
          <v-btn color="primary" @click="save"
                 :disabled="!(valid && contextId)">Save
          </v-btn>
          <v-btn color="primary"
                   :disabled="!valid"
                   @click="create">Create
            </v-btn>
            <v-btn color="secondary" to="/schema"
                   :disabled="!contextId">Create Schema
            </v-btn>
        </v-card-actions>
    </v-card>
</template>

<script>
    import Repository from '@/api/SchemataRepository'
    import selectboxLoaders from '@/mixins/selectbox-loaders'
    import validationRules from '@/mixins/form-validation-rules'

    export default {
        mixins: [selectboxLoaders, validationRules],


        data: () => {
            return {
                organizationId: undefined,
                unitId: undefined,
                contextId: undefined,
                namespace: '',
                description: '',
            }
        },

        methods: {
            create() {
                let vm = this
                Repository.createContext(
                    this.organizationId,
                    this.unitId,
                    this.namespace,
                    this.description)
                    .then((created) => {
                            vm.context = created
                            vm.contextId = created.contextId
                            vm.namespace = created.namespace
                            vm.description = created.description

                            vm.$store.commit('raiseNotification', {
                                message: `Context '${vm.namespace}' created.`,
                                type: 'success'
                            })
                        }
                    )
                    .catch(function (err) {
                        let response = err.response ? err.response.data + ' - ' : ''
                        vm.$store.commit('raiseError', {message: response + err})
                    })
            },
          save() {
            let vm = this
            Repository.updateContext(this.organizationId, this.unitId, this.contextId, this.namespace, this.description)
              .then(() => {
                vm.$store.commit('raiseNotification', {
                  message: `Context ${vm.name} updated.`,
                  type: 'success'
                })
              })
          },
          load(organizationId, unitId, contextId) {
            let vm = this
            Repository.getContext(organizationId, unitId, contextId)
              .then((loaded) => {
                vm.organizationId = loaded.organizationId
                vm.unitId = loaded.unitId
                vm.contextId = loaded.contextId
                vm.namespace = loaded.namespace
                vm.description = loaded.description
              })
          }
        },
      mounted() {
        this.organizationId = this.$store.getters.organizationId
        this.unitId = this.$store.getters.unitId
        let contextId = this.$store.getters.contextId
        if (this.organizationId && this.unitId && contextId) {
          this.load(this.organizationId, this.unitId, contextId)
        }
      }
    }
</script>
